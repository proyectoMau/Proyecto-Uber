    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnAceptarActionPerformed
    {//GEN-HEADEREND:event_btnAceptarActionPerformed
        nS = TxtNumSocio.getText();
        nomS = Manipulacion.buscarSocioNom(VtnPrincipal.s, Manipulacion.buscarSocio(VtnPrincipal.s, nS));
        pos=Integer.parseInt(nS);
        if (nomS == "nul")
        {
            Mensajes.exito(this, "usuario no encontrado");
        } else
        {
            new VtnSocioDashboard().setVisible(true);
            this.setVisible(false);
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

